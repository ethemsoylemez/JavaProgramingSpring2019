package day12_switch_ternary;

public class SwitchScore {
	public static void main(String[] args) {

		int score = 9;

		switch (score) {
		case 1:
			System.out.println("Score is " + score);
			break;
		case 2:
			System.out.println("Score is " + score);
			break;
		case 3:
			System.out.println("Score is " + score);
			break;
		default:
			System.out.println("Invalid score");

		}

	}
}
