package day20_forloop;

public class FoorLoop1 {

	public static void main(String[] args) {
		for (int i = 0; i < 10; i++) {
			System.out.println("Love java!");
		}
		System.out.println("=============");
		for(int j = 5; j<=15; j++) {
			
			System.out.println("Love Java Again!");
		}
		System.out.println("===============");
		for(int num = 20; num>=10;num--) {
			System.out.println("Love Java Again and Again!");
		}
	}
}
