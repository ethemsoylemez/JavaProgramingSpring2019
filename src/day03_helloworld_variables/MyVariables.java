package day03_helloworld_variables;

public class MyVariables {
	public static void main(String[] args) {

		int n;
		n = 100;
		System.out.println(n);

		// This is a comment.
		// it will be ignored by java

		int a;
		a = 3456;
		System.out.println(a);

		a = 1000;
		System.out.println(a);

		String name = "Idil";
		System.out.println(name);
	}
}
